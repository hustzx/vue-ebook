 <!--此组件可以作为书签通用组件，通过接受父组件的参数进行调节-->
<template>
  <div class="bookmark" :style="style" ref="bookmark">
  </div>
</template>

<script>
  import { px2rem } from '../../utils/utils'
  export default {
    props: {
      width: Number,
      height: Number,
      color: String
    },
    computed: {
      style () {
        if (this.color) {
          return {
            borderColor: `${this.color} ${this.color} transparent`
          }
        } else {
          return {}
        }
      }
    },
    methods: {
      // 刷新宽高
      refresh () {
        if (this.height && this.width) {
          this.$refs.bookmark.style.borderWidth = `${px2rem(this.height - 5)}rem ${px2rem(this.width / 2)}rem
          ${px2rem(5)}rem`
        }
      }
    },
    mounted () {
      this.refresh()
    }
  }
</script>

<style lang='scss' scoped>
  @import "../../assets/styles/global";
  .bookmark{
    // 利用边距设置三角形
    width:0;
    height:0;
    border-width: px2rem(50) px2rem(10) px2rem(10);
    border-style: solid;
    border-color: white white transparent;
  }
</style>
